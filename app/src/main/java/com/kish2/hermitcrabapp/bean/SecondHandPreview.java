package com.kish2.hermitcrabapp.bean;

public class SecondHandPreview {

    private String productImg;

    private String productTag;

    private String venderImg;

    private String venderName;

    private double productPrice;

    private long desirePeople;

    private String productTitle;

    public String getProductImg() {
        return productImg;
    }

    public void setProductImg(String productImg) {
        this.productImg = productImg;
    }

    public String getProductTag() {
        return productTag;
    }

    public void setProductTag(String productTag) {
        this.productTag = productTag;
    }

    public String getVenderImg() {
        return venderImg;
    }

    public void setVenderImg(String venderImg) {
        this.venderImg = venderImg;
    }

    public String getVenderName() {
        return venderName;
    }

    public void setVenderName(String venderName) {
        this.venderName = venderName;
    }

    public double getProductPrice() {
        return productPrice;
    }

    public void setProductPrice(double productPrice) {
        this.productPrice = productPrice;
    }

    public long getDesirePeople() {
        return desirePeople;
    }

    public void setDesirePeople(long desirePeople) {
        this.desirePeople = desirePeople;
    }

    public String getProductTitle() {
        return productTitle;
    }

    public void setProductTitle(String productTitle) {
        this.productTitle = productTitle;
    }
}
